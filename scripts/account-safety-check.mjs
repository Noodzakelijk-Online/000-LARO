#!/usr/bin/env node
/**
 * Phase 100 — real-provider cleanup & account safety.
 *
 * Static safety checks that no real credentials or accounts can leak from the
 * shipped artifact. Runnable; fails (non-zero) on any HIGH finding.
 *
 * Checks:
 *   1. `.env` (and secret variants) are NOT tracked in git.
 *   2. `.env` is NOT included in the electron-builder `build.files` / extraResources
 *      (Phase 030 regression guard — secrets must never ship in the installer).
 *   3. `.env` variants are gitignored.
 *   4. No obvious hardcoded secrets in runtime source (OpenAI `sk-`, AWS `AKIA`,
 *      Google `AIza`, Slack `xox`, private-key headers, Stripe `sk_live`).
 *
 * Usage: node scripts/account-safety-check.mjs [--write]
 */
import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from 'fs';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(ROOT, 'docs', 'ACCOUNT_SAFETY.md');

const RUNTIME_DIRS = ['server', 'src-main', 'shared'];
const SKIP = new Set(['node_modules', 'dist', '.git', 'drizzle', 'assets']);
const EXT = new Set(['.ts', '.tsx', '.js', '.mjs', '.cjs', '.json']);

const SECRET_PATTERNS = [
  { name: 'OpenAI key', re: /sk-[A-Za-z0-9]{20,}/ },
  { name: 'AWS access key id', re: /AKIA[0-9A-Z]{16}/ },
  { name: 'Google API key', re: /AIza[0-9A-Za-z_-]{35}/ },
  { name: 'Slack token', re: /xox[baprs]-[0-9A-Za-z-]{10,}/ },
  { name: 'Stripe live key', re: /sk_live_[0-9A-Za-z]{20,}/ },
  { name: 'Private key header', re: /-----BEGIN (?:RSA |EC )?PRIVATE KEY-----/ },
];

function walk(dir, acc) {
  let entries;
  try { entries = readdirSync(join(ROOT, dir)); } catch { return acc; }
  for (const name of entries) {
    if (SKIP.has(name)) continue;
    const rel = join(dir, name);
    let st;
    try { st = statSync(join(ROOT, rel)); } catch { continue; }
    if (st.isDirectory()) walk(rel, acc);
    else if (EXT.has(rel.slice(rel.lastIndexOf('.')))) acc.push(rel);
  }
  return acc;
}

const findings = [];
function add(severity, check, detail) { findings.push({ severity, check, detail }); }

// 1 + 3: git-tracked env files / gitignore
let tracked = '';
try { tracked = execSync('git ls-files', { cwd: ROOT }).toString(); } catch { /* not a repo */ }
const trackedEnv = tracked.split('\n').filter((f) => /^\.env($|\.)/.test(f) && f !== '.env.example');
if (trackedEnv.length > 0) add('HIGH', 'env not tracked', `Tracked secret files: ${trackedEnv.join(', ')}`);
else add('OK', 'env not tracked', 'No real .env variants are tracked (only .env.example).');

const gitignore = existsSync(join(ROOT, '.gitignore')) ? readFileSync(join(ROOT, '.gitignore'), 'utf8') : '';
if (/^\.env(\s|$)/m.test(gitignore)) add('OK', 'env gitignored', '.env is gitignored.');
else add('HIGH', 'env gitignored', '.env is NOT in .gitignore.');

// 2: electron-builder packaging
const pkg = JSON.parse(readFileSync(join(ROOT, 'package.json'), 'utf8'));
const buildFiles = JSON.stringify(pkg.build?.files || []);
const extra = JSON.stringify(pkg.build?.extraResources || []);
if (/\.env/.test(buildFiles) || /\.env/.test(extra)) {
  add('HIGH', 'installer excludes .env', 'electron-builder config references .env — secrets could ship.');
} else {
  add('OK', 'installer excludes .env', 'No .env in build.files/extraResources (Phase 030 guard holds).');
}

// 4: hardcoded secrets in runtime source
const runtimeFiles = RUNTIME_DIRS.flatMap((d) => walk(d, []));
let secretHits = 0;
for (const f of runtimeFiles) {
  const text = readFileSync(join(ROOT, f), 'utf8');
  for (const p of SECRET_PATTERNS) {
    if (p.re.test(text)) { add('HIGH', 'no hardcoded secrets', `${p.name} pattern in ${f}`); secretHits++; }
  }
}
if (secretHits === 0) add('OK', 'no hardcoded secrets', `Scanned ${runtimeFiles.length} runtime files; no secret patterns.`);

const high = findings.filter((f) => f.severity === 'HIGH');

const out = [];
out.push('# Account Safety & Real-Provider Cleanup (Phase 100)');
out.push('');
out.push('> Generated by `node scripts/account-safety-check.mjs`. Do not edit by hand.');
out.push('');
out.push(`**${high.length}** HIGH finding(s). ${high.length === 0 ? 'Safe to package. ✅' : 'MUST fix before release. ❌'}`);
out.push('');
out.push('| Check | Severity | Detail |');
out.push('|---|---|---|');
for (const f of findings) out.push(`| ${f.check} | ${f.severity} | ${f.detail} |`);
out.push('');
out.push('## Operator cleanup checklist (before distributing / going live)');
out.push('- Rotate any provider keys used during development; never ship a dev `.env`.');
out.push('- Configure production credentials via the host environment, not files in the bundle.');
out.push('- Verify `system.providerChecklist` (authenticated) shows only intended providers.');
out.push('- Remove demo/seed accounts and demo-labelled data from any production database.');
out.push('- Confirm `NODE_ENV=production` disables demo mode (`system.appInfo.demoMode=false`).');
out.push('');

const text = out.join('\n');
if (process.argv.includes('--write')) {
  writeFileSync(OUT, text + '\n');
  console.log(`account-safety: wrote ${OUT}`);
} else {
  console.log(text);
}
console.error(`account-safety: ${high.length} HIGH, ${findings.length - high.length} OK.`);
process.exit(high.length === 0 ? 0 : 1);
